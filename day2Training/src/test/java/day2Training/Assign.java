package day2Training;

public class Assign {

	public int addtion (int a, int b)
	{
		int c = a+b;
		return c;
	}
	
	public int subtraction(int d, int e)
	{
		int f = d - e;
		return f;
	}

	public int multiplication(int g, int h)
	{
		int i = g*h;
		return i;
	}
	
	public double division(int k, int l)
	{
		double m = k/l;
		return m;
	}
}