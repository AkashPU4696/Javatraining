package day5Scanner;

public class Calculator2 {

	public int add(int a, int b)
	{
		int c=a+b;
		return c;
	}
	
	public int sub(int m, int n)
	{
		int o = m-n;
		return o;
	}
	
	public int mul(int r, int s)
	{
		int p = r*s;
		return p;
	}
	
	public double div(double u, double v)
	{
		double w=u/v;
		return w;
	}
}
