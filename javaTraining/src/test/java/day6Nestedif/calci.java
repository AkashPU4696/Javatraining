package day6Nestedif;

public class calci {

	public int add(int a,int b)
	{
		int c = a+b;
		return c;
	}
	public int sub(int g, int h)
	{
		int k = g-h;
		return k;
	}
	public int mul(int m, int n)
	{
		int o=m*n;
		return o;
	}
	public double div(int r, int s)
	{
		double p=r/s;
		return p;
	}
}
